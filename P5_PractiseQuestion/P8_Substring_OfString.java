package P5_PractiseQuestion;

public class P8_Substring_OfString {
	
	public static void main(String[] args) {
		
		String str = "vishwakarma";
		int len = str.length();
		
		System.out.println("Length of the given string is : "+len);
		
		System.out.println("Substring from the begining : "+str.substring(2));
		
		System.out.println("Substring for start and end : "+str.substring(4, 10));
	}

}
